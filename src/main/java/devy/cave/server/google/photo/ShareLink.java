package devy.cave.server.google.photo;

public class ShareLink {

    private String ogImage;
    private String ogVideo;
    private String ogSecretVideo;

    public String getOgImage() {
        return ogImage;
    }

    public void setOgImage(String ogImage) {
        this.ogImage = ogImage;
    }

    public String getOgVideo() {
        return ogVideo;
    }

    public void setOgVideo(String ogVideo) {
        this.ogVideo = ogVideo;
    }

    public String getOgSecretVideo() {
        return ogSecretVideo;
    }

    public void setOgSecretVideo(String ogSecretVideo) {
        this.ogSecretVideo = ogSecretVideo;
    }
}